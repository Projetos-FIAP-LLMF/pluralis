CREATE TABLE employees (
    id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100) NOT NULL,
    email VARCHAR2(100) NOT NULL,
    gender VARCHAR2(50) NOT NULL,
    ethnicity VARCHAR2(50) NOT NULL,
    neurodivergent NUMBER(1) NOT NULL,
    lgbtqia NUMBER(1) NOT NULL
);

CREATE TABLE trainings (
    id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100) NOT NULL,
    date DATE NOT NULL,
    mandatory NUMBER(1) NOT NULL
);

CREATE TABLE training_participations (
    id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    employee_id NUMBER NOT NULL,
    training_id NUMBER NOT NULL,
    completed NUMBER(1) NOT NULL,
    CONSTRAINT fk_employee FOREIGN KEY (employee_id) REFERENCES employees(id),
    CONSTRAINT fk_training FOREIGN KEY (training_id) REFERENCES trainings(id)
);

CREATE TABLE anonymous_feedback (
    id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    message VARCHAR2(500) NOT NULL,
    sent_date DATE DEFAULT SYSDATE
);
